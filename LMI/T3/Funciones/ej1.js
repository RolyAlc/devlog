// Definición de la función doble
function doble(num) {
    return num * 2;
}

//Llamados a la función con dif valores
console.log("El doble de 3 es: ", doble(3));
console.log("El doble de 7 es: ", doble(7));
console.log("El doble de 10 es: ", doble(10));
console.log("El doble de -5 es: ", doble(-5));